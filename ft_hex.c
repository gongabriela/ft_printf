#include "ft_printf.h"

int	ft_hex(unsigned int n, char *base)
{
	int	i;

	i = 0;
	if (n >= 16)
		i += ft_hex(n / 16, base);
	i += write(1, &base[n % 16], 1);
	return (i);
}
